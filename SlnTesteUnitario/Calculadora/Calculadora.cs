namespace Exemplo
{
    public class Calculadora
    {
        public int Somar(int x, int y)
        {
            return x + y;
        }

        public int Substrair(int x, int y)
        {
            return x - y;
        }

        public int Multiplicar(int x, int y)
        {
            return x * y;
        }

        public int Dividir(int x, int y)
        {
            return x / y;
        }
    }
}
